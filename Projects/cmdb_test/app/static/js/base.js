$('.tree-1').click(function () {
    var i1 = document.getElementById('tree-1').style.display
    var i2 = document.getElementById('tree-2').style.display
    var i3 = document.getElementById('tree-3').style.display
    var i4 = document.getElementById('tree-4').style.display
    var i5 = document.getElementById('tree-5').style.display
    var i6 = document.getElementById('tree-6').style.display

    if (i1.length  == 0){
        var i1 = 'none'
    }
    if (i2.length  == 0){
        var i2 = 'none'
    }
    if (i3.length  == 0){
        var i3 = 'none'
    }
    if (i4.length  == 0){
        var i4 = 'none'
    }
    if (i5.length  == 0){
        var i5 = 'none'
    }
    if (i6.length  == 0){
        var i6 = 'none'
    }
    // alert('1:'+i1+'2:'+i2+'3:'+i3+'4:'+i4+'5:'+i5+'6:'+i6)
    $.cookie('display1', i1,  { expires: 1 })
    $.cookie('display2', i2,  { expires: 1 })
    $.cookie('display3', i3,  { expires: 1 })
    $.cookie('display4', i4,  { expires: 1 })
    $.cookie('display5', i5,  { expires: 1 })
    $.cookie('display6', i6,  { expires: 1 })

})
